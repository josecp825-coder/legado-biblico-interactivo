function filtrarHimnosCat(texto) {
    var res = document.getElementById('himn-cat-resultados');
    var grupos = document.getElementById('himn-cat-grupos');
    if (!texto || texto.length < 2) { res.style.display = 'none'; grupos.style.display = 'block'; return; }
    grupos.style.display = 'none'; res.style.display = 'block';
    var t = texto.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    var esNum = /^\d+$/.test(texto.trim());
    var encontrados = [];
    Object.entries(HIMNARIO_ADVENTISTA).forEach(function(e) {
        var num = parseInt(e[0]), titulo = e[1];
        var tNorm = titulo.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
        if (esNum ? num.toString().startsWith(texto.trim()) : tNorm.includes(t)) {
            // Buscar categoría
            var catNombre = '';
            HIMNARIO_CATEGORIAS.forEach(function(g) { g.cats.forEach(function(c) { if (c.nums.includes(num)) catNombre = c.nombre; }); });
            encontrados.push({ num: num, titulo: titulo, cat: catNombre });
        }
    });
    if (encontrados.length === 0) {
        res.innerHTML = '<div style="text-align:center;color:rgba(255,255,255,0.4);padding:20px;font-size:0.85rem;">Sin resultados</div>';
    } else {
        var h = '<div style="display:grid;gap:4px;">';
        encontrados.slice(0, 20).forEach(function(r) {
            h += '<div style="display:flex;align-items:center;gap:10px;padding:10px 12px;background:rgba(255,255,255,0.04);border-radius:10px;border-left:3px solid #a29bfe;">';
            h += '<div style="color:#a29bfe;font-size:0.85rem;font-weight:900;min-width:32px;text-align:center;">' + r.num + '</div>';
            h += '<div style="flex:1;"><div style="color:#fff;font-size:0.82rem;font-weight:600;">' + r.titulo + '</div>';
            if (r.cat) h += '<div style="color:rgba(255,255,255,0.3);font-size:0.6rem;margin-top:1px;">' + r.cat + '</div>';
            h += '</div></div>';
        });
        h += '</div>';
        if (encontrados.length > 20) h += '<div style="text-align:center;color:rgba(255,255,255,0.3);font-size:0.7rem;margin-top:8px;">...y ' + (encontrados.length - 20) + ' más</div>';
        res.innerHTML = h;
    }
}

