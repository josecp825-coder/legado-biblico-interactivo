function buscarHimno(numero) {
    return HIMNARIO_ADVENTISTA[numero] || null;
}

// Función auxiliar para buscar himnos por texto
function buscarHimnosPorTexto(texto) {
    if (!texto || texto.length < 2) return [];
    const t = texto.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    const resultados = [];
    Object.entries(HIMNARIO_ADVENTISTA).forEach(([num, titulo]) => {
        const tNorm = titulo.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
        if (tNorm.includes(t)) {
            resultados.push({ num: parseInt(num), titulo });
        }
    });
    return resultados.slice(0, 8);
}

